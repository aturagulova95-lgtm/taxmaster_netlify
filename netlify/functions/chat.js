exports.handler = async function(event, context) {
  const headers = {
    "Content-Type": "application/json",
    "Access-Control-Allow-Origin": "*",
    "Access-Control-Allow-Headers": "Content-Type",
    "Access-Control-Allow-Methods": "POST, OPTIONS"
  };

  if (event.httpMethod === "OPTIONS") {
    return { statusCode: 200, headers, body: "" };
  }

  if (event.httpMethod !== "POST") {
    return { statusCode: 405, headers, body: JSON.stringify({ reply: "Method not allowed" }) };
  }

  if (!process.env.OPENAI_API_KEY) {
    return {
      statusCode: 200,
      headers,
      body: JSON.stringify({ reply: "Бот временно недоступен. Напишите Жанибеку: WhatsApp +7 747 658 94 12" })
    };
  }

  let messages;
  try {
    const body = JSON.parse(event.body || "{}");
    messages = body.messages;
    if (!messages || !Array.isArray(messages) || messages.length === 0) throw new Error("no messages");
  } catch(e) {
    return { statusCode: 400, headers, body: JSON.stringify({ reply: "Неверный формат запроса" }) };
  }

  try {
    const res = await fetch("https://api.openai.com/v1/chat/completions", {
      method: "POST",
      headers: {
        "Content-Type": "application/json",
        "Authorization": "Bearer " + process.env.OPENAI_API_KEY
      },
      body: JSON.stringify({
        model: "gpt-4o-mini",
        messages: [{ role: "system", content: SYSTEM_PROMPT }, ...messages],
        temperature: 0.3,
        max_tokens: 900
      })
    });

    if (!res.ok) {
      const errText = await res.text();
      console.error("OpenAI error:", res.status, errText);
      const errMap = {
        401: "Неверный API ключ.",
        402: "Закончился баланс OpenAI. Напишите: WhatsApp +7 747 658 94 12",
        429: "Слишком много запросов, подождите минуту. Или напишите: WhatsApp +7 747 658 94 12"
      };
      return {
        statusCode: 200,
        headers,
        body: JSON.stringify({ reply: (errMap[res.status] || "Ошибка " + res.status) + " Напишите: WhatsApp +7 747 658 94 12" })
      };
    }

    const data = await res.json();
    const reply = data.choices[0].message.content.trim();
    return { statusCode: 200, headers, body: JSON.stringify({ reply }) };

  } catch(err) {
    console.error("Error:", err.message);
    return {
      statusCode: 200,
      headers,
      body: JSON.stringify({ reply: "Ошибка соединения. Напишите Жанибеку: WhatsApp +7 747 658 94 12" })
    };
  }
};
