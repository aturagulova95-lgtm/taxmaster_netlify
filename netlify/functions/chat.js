exports.handler = async function(event) {
  const headers = {
    "Content-Type": "application/json",
    "Access-Control-Allow-Origin": "*",
    "Access-Control-Allow-Headers": "Content-Type",
    "Access-Control-Allow-Methods": "POST, OPTIONS"
  };

  if (event.httpMethod === "OPTIONS") return { statusCode: 200, headers, body: "" };
  if (event.httpMethod !== "POST") return { statusCode: 405, headers, body: JSON.stringify({ reply: "Method not allowed" }) };
  if (!process.env.ANTHROPIC_API_KEY) return { statusCode: 200, headers, body: JSON.stringify({ reply: "Бот недоступен. Пишите: WhatsApp +7 747 658 94 12" }) };

  let messages;
  try {
    messages = JSON.parse(event.body || "{}").messages;
    if (!messages || !Array.isArray(messages)) throw new Error();
  } catch { return { statusCode: 400, headers, body: JSON.stringify({ reply: "Ошибка запроса" }) }; }

  try {
    const res = await fetch("https://api.anthropic.com/v1/messages", {
      method: "POST",
      headers: {
        "Content-Type": "application/json",
        "x-api-key": process.env.ANTHROPIC_API_KEY,
        "anthropic-version": "2023-06-01"
      },
      body: JSON.stringify({
        model: "claude-haiku-4-5-20251001",
        max_tokens: 1024,
        system: SYSTEM,
        messages: messages
      })
    });

    if (!res.ok) {
      const errText = await res.text();
      console.error("Anthropic error:", res.status, errText);
      const errMap = {
        401: "Неверный API ключ Anthropic.",
        429: "Слишком много запросов, подождите.",
        529: "Сервис временно перегружен, попробуйте через минуту."
      };
      return { statusCode: 200, headers, body: JSON.stringify({ reply: (errMap[res.status] || "Ошибка " + res.status) + " Пишите: WhatsApp +7 747 658 94 12" }) };
    }

    const data = await res.json();
    const reply = data.content[0].text.trim();
    return { statusCode: 200, headers, body: JSON.stringify({ reply }) };

  } catch(err) {
    console.error("Error:", err.message);
    return { statusCode: 200, headers, body: JSON.stringify({ reply: "Ошибка соединения. Пишите: WhatsApp +7 747 658 94 12" }) };
  }
};
